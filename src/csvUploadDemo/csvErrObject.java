package csvUploadDemo;

public class csvErrObject {
	
	// 項目No
	public int komokuNo;
	// ERRコード
	public String errCode;
	
	public int getKomokuNo() {
		return komokuNo;
	}
	public void setKomokuNo(int komokuNo) {
		this.komokuNo = komokuNo;
	}
	public String getErrCode() {
		return errCode;
	}
	public void setErrCode(String errCode) {
		this.errCode = errCode;
	}
}
